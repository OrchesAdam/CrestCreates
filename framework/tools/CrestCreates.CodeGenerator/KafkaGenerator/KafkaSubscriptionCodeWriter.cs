using System.Text;

namespace CrestCreates.CodeGenerator.KafkaGenerator
{
    internal sealed class KafkaSubscriptionCodeWriter
    {
        public string WriteRegistry(KafkaSubscriptionModel model)
        {
            var sb = new StringBuilder();

            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("#nullable enable");
            sb.AppendLine("using System;");
            sb.AppendLine("using System.Collections.Generic;");
            sb.AppendLine("using System.Threading;");
            sb.AppendLine("using System.Threading.Tasks;");
            sb.AppendLine("using Microsoft.Extensions.DependencyInjection;");
            sb.AppendLine("using CrestCreates.EventBus.Kafka.Options;");
            sb.AppendLine();

            sb.AppendLine($"namespace {model.Namespace}");
            sb.AppendLine("{");

            WriteRegistryClass(sb, model);

            sb.AppendLine("}");

            return sb.ToString();
        }

        private void WriteRegistryClass(StringBuilder sb, KafkaSubscriptionModel model)
        {
            sb.AppendLine("    public static class KafkaSubscriptionRegistry");
            sb.AppendLine("    {");
            sb.AppendLine("        public static IReadOnlyList<KafkaSubscriptionInfo> GetSubscriptions() => _subscriptions;");
            sb.AppendLine();

            // Build the static array initialization
            sb.AppendLine("        private static readonly IReadOnlyList<KafkaSubscriptionInfo> _subscriptions = new[]");
            sb.AppendLine("        {");

            foreach (var subscription in model.Subscriptions)
            {
                var consumerGroup = subscription.ConsumerGroup ?? "crestcreates-consumers";

                sb.AppendLine($"            new KafkaSubscriptionInfo(");
                sb.AppendLine($"                Topic: \"{EscapeString(subscription.Topic)}\",");
                sb.AppendLine($"                EventType: typeof(global::{subscription.EventTypeFullName}),");
                sb.AppendLine($"                HandlerType: typeof(global::{subscription.HandlerTypeFullName}),");
                sb.AppendLine($"                HandlerMethod: \"{EscapeString(subscription.HandlerMethodName)}\",");
                sb.AppendLine($"                ConsumerGroup: \"{EscapeString(consumerGroup)}\",");
                sb.AppendLine($"                MaxPollRecords: {subscription.MaxPollRecords},");
                WriteInvokerLambda(sb, subscription);
                sb.AppendLine($"            ),");
            }

            sb.AppendLine("        };");

            sb.AppendLine("    }");
        }

        private void WriteInvokerLambda(StringBuilder sb, KafkaSubscriptionInfo subscription)
        {
            // Generate a lambda that:
            // 1. Resolves the handler from DI
            // 2. Calls the handler method with the event and cancellation token
            sb.AppendLine($"                InvokeHandler: (sp, evt, ct) =>");
            sb.AppendLine($"                {{");
            sb.AppendLine($"                    var handler = (global::{subscription.HandlerTypeFullName})sp.GetRequiredService(typeof(global::{subscription.HandlerTypeFullName}));");
            sb.AppendLine($"                    return handler.{subscription.HandlerMethodName}((global::{subscription.EventTypeFullName})evt!, ct);");
            sb.AppendLine($"                }})");
        }

        private static string EscapeString(string value)
        {
            if (string.IsNullOrEmpty(value))
                return value;

            return value.Replace("\\", "\\\\").Replace("\"", "\\\"");
        }
    }
}
