using System.Collections.Generic;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;

namespace CrestCreates.CodeGenerator.ObjectMappingGenerator
{
    internal sealed class ObjectMappingCodeWriter
    {
        public string Write(ObjectMappingModel model)
        {
            var sb = new StringBuilder();
            var declaration = model.Declaration;

            sb.AppendLine("// <auto-generated />");
            sb.AppendLine("#nullable enable");
            sb.AppendLine("using System;");
            sb.AppendLine("using System.Linq.Expressions;");
            sb.AppendLine();

            if (!string.IsNullOrEmpty(declaration.Namespace))
            {
                sb.AppendLine($"namespace {declaration.Namespace}");
                sb.AppendLine("{");
            }

            WriteClass(sb, model);

            if (!string.IsNullOrEmpty(declaration.Namespace))
            {
                sb.AppendLine("}");
            }

            return sb.ToString();
        }

        private void WriteClass(StringBuilder sb, ObjectMappingModel model)
        {
            var declaration = model.Declaration;
            var sourceType = declaration.SourceType.ToDisplayString();
            var targetType = declaration.TargetType.ToDisplayString();

            sb.AppendLine($"    public static partial class {declaration.MapperClassName}");
            sb.AppendLine("    {");

            // Generate ToTarget method if needed
            if (declaration.Direction is MapDirection.Create or MapDirection.Both)
            {
                WriteToTargetMethod(sb, model, sourceType, targetType);
                sb.AppendLine();
            }

            // Generate Apply method if needed
            if (declaration.Direction is MapDirection.Apply or MapDirection.Both)
            {
                WriteApplyMethod(sb, model, sourceType, targetType);
                sb.AppendLine();
            }

            // Generate ToTargetExpression if needed
            if (declaration.Direction is MapDirection.Create or MapDirection.Both)
            {
                WriteToTargetExpression(sb, model, sourceType, targetType);
                sb.AppendLine();
            }

            // Generate partial method declarations
            WritePartialMethods(sb, model, sourceType, targetType);

            sb.AppendLine("    }");
        }

        private void WriteToTargetMethod(
            StringBuilder sb,
            ObjectMappingModel model,
            string sourceType,
            string targetType)
        {
            sb.AppendLine("        /// <summary>");
            sb.AppendLine($"        /// Maps {sourceType} to {targetType} (creates new instance).");
            sb.AppendLine("        /// </summary>");
            sb.AppendLine($"        public static {targetType} ToTarget({sourceType} source)");
            sb.AppendLine("        {");
            sb.AppendLine("            if (source is null)");
            sb.AppendLine("                throw new ArgumentNullException(nameof(source));");
            sb.AppendLine();
            sb.AppendLine($"            var result = new {targetType}");
            sb.AppendLine("            {");

            var mappings = model.PropertyMappings
                .Where(m => !m.IsIgnored && !m.IsReadOnly)
                .ToList();

            for (int i = 0; i < mappings.Count; i++)
            {
                var mapping = mappings[i];
                var comma = i < mappings.Count - 1 ? "," : "";
                var valueExpression = GetPropertyAssignmentExpression(mapping);
                sb.AppendLine($"                {mapping.TargetPropertyName} = {valueExpression}{comma}");
            }

            sb.AppendLine("            };");
            sb.AppendLine();
            sb.AppendLine("            AfterToTarget(source, result);");
            sb.AppendLine("            return result;");
            sb.AppendLine("        }");
        }

        private string GetPropertyAssignmentExpression(PropertyMapping mapping)
        {
            string sourceExpression;

            if (mapping.NavigationSegments != null && mapping.NavigationSegments.Count > 1)
            {
                sourceExpression = BuildNavigationExpression(mapping);
            }
            else
            {
                sourceExpression = $"source.{mapping.SourceProperty.Name}";
            }

            // Custom converter
            if (mapping.ConverterTypeFullName != null)
            {
                sourceExpression = $"{mapping.ConverterTypeFullName}.Convert({sourceExpression})";
            }

            // Handle collection conversion
            if (mapping.NeedsCollectionConversion && mapping.CollectionConversionMethod != null)
            {
                sourceExpression = $"{sourceExpression}.{mapping.CollectionConversionMethod}";
            }

            // Handle null check
            if (mapping.NeedsNullCheck)
            {
                var typeName = mapping.TargetProperty?.Type.ToDisplayString()
                    ?? mapping.SourceProperty.Type.ToDisplayString();
                var defaultValue = GetDefaultValue(typeName);
                return $"{sourceExpression} ?? {defaultValue}";
            }

            return sourceExpression;
        }

        private string BuildNavigationExpression(PropertyMapping mapping)
        {
            var segments = mapping.NavigationSegments!;
            var firstProp = mapping.SourceProperty;

            var parts = new List<string> { $"source.{segments[0]}" };

            INamedTypeSymbol? currentType = firstProp.Type as INamedTypeSymbol;
            for (int i = 1; i < segments.Count; i++)
            {
                if (currentType == null) break;
                // Use ?. for reference type intermediate segments
                bool isRefType = currentType.IsReferenceType
                    || (currentType.OriginalDefinition.SpecialType == SpecialType.System_Nullable_T);
                string op = isRefType ? "?." : ".";
                parts.Add($"{op}{segments[i]}");

                var segmentProp = currentType.GetMembers(segments[i])
                    .OfType<IPropertySymbol>().FirstOrDefault(p => !p.IsStatic);
                currentType = segmentProp?.Type as INamedTypeSymbol;
            }

            return string.Concat(parts);
        }

        private string GetDefaultValue(string typeName)
        {
            // Remove nullable suffix if present
            if (typeName.EndsWith("?"))
            {
                typeName = typeName.Substring(0, typeName.Length - 1);
            }

            // Handle common types
            if (typeName == "string")
                return "string.Empty";
            if (typeName == "int" || typeName == "Int32")
                return "0";
            if (typeName == "long" || typeName == "Int64")
                return "0L";
            if (typeName == "double")
                return "0.0";
            if (typeName == "float")
                return "0.0f";
            if (typeName == "decimal")
                return "0m";
            if (typeName == "bool" || typeName == "Boolean")
                return "false";
            if (typeName == "Guid")
                return "Guid.Empty";
            if (typeName == "DateTime")
                return "DateTime.MinValue";
            if (typeName == "DateTimeOffset")
                return "DateTimeOffset.MinValue";

            // For reference types and other types, use default()
            return $"default({typeName})";
        }

        private void WriteApplyMethod(
            StringBuilder sb,
            ObjectMappingModel model,
            string sourceType,
            string targetType)
        {
            sb.AppendLine("        /// <summary>");
            sb.AppendLine($"        /// Applies {sourceType} values to existing {targetType}.");
            sb.AppendLine("        /// </summary>");
            sb.AppendLine($"        public static void Apply({sourceType} source, {targetType} destination)");
            sb.AppendLine("        {");
            sb.AppendLine("            if (source is null)");
            sb.AppendLine("                throw new ArgumentNullException(nameof(source));");
            sb.AppendLine("            if (destination is null)");
            sb.AppendLine("                throw new ArgumentNullException(nameof(destination));");
            sb.AppendLine();
            sb.AppendLine("            BeforeApply(source, destination);");

            foreach (var mapping in model.PropertyMappings.Where(m => !m.IsIgnored && !m.IsReadOnly))
            {
                var valueExpression = GetPropertyAssignmentExpression(mapping);
                sb.AppendLine($"            destination.{mapping.TargetPropertyName} = {valueExpression};");
            }

            sb.AppendLine("            AfterApply(source, destination);");
            sb.AppendLine("        }");
        }

        private void WriteToTargetExpression(
            StringBuilder sb,
            ObjectMappingModel model,
            string sourceType,
            string targetType)
        {
            sb.AppendLine("        /// <summary>");
            sb.AppendLine("        /// Projection expression for query pipelines.");
            sb.AppendLine("        /// </summary>");
            sb.AppendLine($"        public static Expression<Func<{sourceType}, {targetType}>> ToTargetExpression =>");
            sb.AppendLine("            source => new " + targetType);
            sb.AppendLine("            {");

            // Skip properties with navigation paths or custom converters (not Expression-compatible)
            var mappings = model.PropertyMappings
                .Where(m => !m.IsIgnored && !m.IsReadOnly
                    && m.NavigationSegments == null
                    && m.ConverterTypeFullName == null)
                .ToList();

            for (int i = 0; i < mappings.Count; i++)
            {
                var mapping = mappings[i];
                var comma = i < mappings.Count - 1 ? "," : "";
                var valueExpression = GetPropertyAssignmentExpression(mapping);
                sb.AppendLine($"                {mapping.TargetPropertyName} = {valueExpression}{comma}");
            }

            sb.AppendLine("            };");
        }

        private void WritePartialMethods(
            StringBuilder sb,
            ObjectMappingModel model,
            string sourceType,
            string targetType)
        {
            if (model.Declaration.Direction is MapDirection.Create or MapDirection.Both)
            {
                sb.AppendLine($"        static partial void AfterToTarget({sourceType} source, {targetType} destination);");
            }

            if (model.Declaration.Direction is MapDirection.Apply or MapDirection.Both)
            {
                sb.AppendLine($"        static partial void BeforeApply({sourceType} source, {targetType} destination);");
                sb.AppendLine($"        static partial void AfterApply({sourceType} source, {targetType} destination);");
            }
        }
    }
}