            sourceCode += "using CrestCreates.Infrastructure.EventBus.Local;\n";
            sourceCode += "\n";
            sourceCode += "namespace CrestCreates.Infrastructure.EventBus\n";
            sourceCode += "{\n";
            sourceCode += "    /// <summary>\n";
            sourceCode += "    /// 事件总线扩展方法\n";
            sourceCode += "    /// </summary>\n";
            sourceCode += "    public static class EventBusExtensions\n";
            sourceCode += "    {\n";
            sourceCode += "        /// <summary>\n";
            sourceCode += "        /// 注册领域事件处理器\n";
            sourceCode += "        /// </summary>\n";
            sourceCode += "        /// <param name=" + """services""" + @">服务集合</param>\n";
            sourceCode += "        /// <param name=" + """assemblies""" + @">包含事件处理器的程序集</param>\n";
            sourceCode += "        /// <returns></returns>\n";
            sourceCode += "        public static IServiceCollection AddDomainEventHandlers(this IServiceCollection services, params Assembly[] assemblies)\n";
            sourceCode += "        {\n";
            sourceCode += "            services.AddMediatR(config =>\n";
            sourceCode += "            {\n";
            sourceCode += "                if (assemblies != null && assemblies.Length > 0)\n";
            sourceCode += "                {\n";
            sourceCode += "                    foreach (var assembly in assemblies)\n";
            sourceCode += "                    {\n";
            sourceCode += "                        config.RegisterServicesFromAssembly(assembly);\n";
            sourceCode += "                    }\n";
            sourceCode += "                }\n";
            sourceCode += "            });\n";
            sourceCode += "            \n";
            sourceCode += "            services.AddScoped<IDomainEventPublisher, DomainEventPublisher>();\n";
            sourceCode += "            \n";
            sourceCode += "            return services;\n";
            sourceCode += "        }\n";
            sourceCode += "        \n";
            sourceCode += "        /// <summary>\n";
            sourceCode += "        /// 注册领域事件处理器（从当前程序集）\n";
            sourceCode += "        /// </summary>\n";
