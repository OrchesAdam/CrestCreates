            sourceCode += "        public static async Task PublishDomainEventsAsync<TEntity, TId>(\n";
            sourceCode += "            this IDomainEventPublisher publisher,\n";
            sourceCode += "            IEnumerable<TEntity> entities,\n";
            sourceCode += "            CancellationToken cancellationToken = default)\n";
            sourceCode += "            where TEntity : Entity<TId>\n";
            sourceCode += "            where TId : IEquatable<TId>\n";
            sourceCode += "        {\n";
            sourceCode += "            foreach (var entity in entities)\n";
            sourceCode += "            {\n";
            sourceCode += "                foreach (var domainEvent in entity.DomainEvents)\n";
            sourceCode += "                {\n";
            sourceCode += "                    await publisher.PublishAsync(domainEvent, cancellationToken);\n";
            sourceCode += "                }\n";
            sourceCode += "                entity.ClearDomainEvents();\n";
            sourceCode += "            }\n";
            sourceCode += "        }\n";
            sourceCode += "    }\n";
            sourceCode += "}";

            context.AddSource("DomainEventHelper.g.cs", SourceText.From(sourceCode, Encoding.UTF8));
        }
    }
