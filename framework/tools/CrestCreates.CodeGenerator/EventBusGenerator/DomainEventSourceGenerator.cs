using System;
using System.Collections.Generic;
using System.Collections.Immutable;
using System.IO;
using System.Linq;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace CrestCreates.CodeGenerator.EventBusGenerator
{
    [Generator]
    public class DomainEventSourceGenerator : IIncrementalGenerator
    {
        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            var domainEvents = context.SyntaxProvider
                .CreateSyntaxProvider(
                    predicate: static (node, _) => IsDomainEventCandidate(node),
                    transform: static (ctx, _) => GetDomainEventClass(ctx))
                .Where(static x => x is not null)
                .Collect();

            var eventHandlers = context.SyntaxProvider
                .CreateSyntaxProvider(
                    predicate: static (node, _) => IsEventHandlerCandidate(node),
                    transform: static (ctx, _) => GetEventHandlerClass(ctx))
                .Where(static x => x is not null)
                .Collect();

            var combined = domainEvents.Combine(eventHandlers);

            context.RegisterSourceOutput(combined, ExecuteGeneration);
        }

        private static bool IsDomainEventCandidate(SyntaxNode node)
        {
            return node is ClassDeclarationSyntax classDeclaration && 
                   classDeclaration.BaseList != null && 
                   classDeclaration.BaseList.Types.Any(t => 
                       t.Type.ToString().Contains("DomainEvent") || 
                       t.Type.ToString().Contains("IDomainEvent"));
        }

        private static bool IsEventHandlerCandidate(SyntaxNode node)
        {
            return node is ClassDeclarationSyntax classDeclaration && 
                   classDeclaration.BaseList != null && 
                   classDeclaration.BaseList.Types.Any(t => 
                       t.Type.ToString().Contains("INotificationHandler<") || 
                       t.Type.ToString().Contains("IEventHandler<"));
        }

        private static INamedTypeSymbol? GetDomainEventClass(GeneratorSyntaxContext context)
        {
            var classDeclaration = (ClassDeclarationSyntax)context.Node;
            var symbol = context.SemanticModel.GetDeclaredSymbol(classDeclaration) as INamedTypeSymbol;
            
            if (symbol != null && IsDomainEvent(symbol))
            {
                return symbol;
            }
            
            return null;
        }

        private static INamedTypeSymbol? GetEventHandlerClass(GeneratorSyntaxContext context)
        {
            var classDeclaration = (ClassDeclarationSyntax)context.Node;
            var symbol = context.SemanticModel.GetDeclaredSymbol(classDeclaration) as INamedTypeSymbol;
            
            if (symbol != null && IsEventHandler(symbol))
            {
                return symbol;
            }
            
            return null;
        }

        private static bool IsDomainEvent(INamedTypeSymbol symbol)
        {
            return symbol.AllInterfaces.Any(i => i.Name == "IDomainEvent") || 
                   (symbol.BaseType?.Name == "DomainEvent") || 
                   (symbol.BaseType?.AllInterfaces.Any(i => i.Name == "IDomainEvent") ?? false);
        }

        private static bool IsEventHandler(INamedTypeSymbol symbol)
        {
            return symbol.AllInterfaces.Any(i => 
                i.Name == "INotificationHandler" || 
                i.Name == "IEventHandler");
        }

        private void ExecuteGeneration(SourceProductionContext context, (ImmutableArray<INamedTypeSymbol?> Events, ImmutableArray<INamedTypeSymbol?> Handlers) data)
        {
            var (events, handlers) = data;

            if (events.IsDefaultOrEmpty && handlers.IsDefaultOrEmpty)
                return;

            GenerateEventBusExtensions(context, events, handlers);
            GenerateDomainEventHelper(context);
        }

        private void GenerateEventBusExtensions(SourceProductionContext context, ImmutableArray<INamedTypeSymbol?> events, ImmutableArray<INamedTypeSymbol?> handlers)
        {
            var eventTypes = events.Where(e => e != null).ToList();
            var handlerTypes = handlers.Where(h => h != null).ToList();

            if (!eventTypes.Any() && !handlerTypes.Any())
                return;

            var sourceCode = "// <auto-generated /\n";
            sourceCode += "using System;\n";
