            sourceCode += "using System.Collections.Generic;\n";
            sourceCode += "using System.Threading;\n";
            sourceCode += "using System.Threading.Tasks;\n";
            sourceCode += "using CrestCreates.Domain.DomainEvents;\n";
            sourceCode += "using CrestCreates.Domain.Entities;\n";
            sourceCode += "\n";
            sourceCode += "namespace CrestCreates.Infrastructure.EventBus\n";
            sourceCode += "{\n";
            sourceCode += "    /// <summary>\n";
            sourceCode += "    /// 领域事件辅助类\n";
            sourceCode += "    /// </summary>\n";
            sourceCode += "    public static class DomainEventHelper\n";
            sourceCode += "    {\n";
            sourceCode += "        /// <summary>\n";
            sourceCode += "        /// 从实体集合中获取所有领域事件\n";
            sourceCode += "        /// </summary>\n";
