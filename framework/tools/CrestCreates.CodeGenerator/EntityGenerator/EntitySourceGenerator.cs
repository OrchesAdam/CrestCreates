using System.Threading.Tasks;
using System.Collections.Generic;
using System.Linq.Expressions;
using {namespaceName};

namespace {namespaceName}.Extensions
{{
    /// <summary>
    /// {entityName} 实体的查询扩展方法
    /// </summary>
    public static class {entityName}QueryExtensions
    {{
        /// <summary>
        /// 分页查询
        /// </summary>
        public static IQueryable<{entityName}> PageBy(this IQueryable<{entityName}> query, int pageNumber, int pageSize)
        {{
            if (pageNumber < 1) pageNumber = 1;
            if (pageSize < 1) pageSize = 10;
            return query.Skip((pageNumber - 1) * pageSize).Take(pageSize);
        }}
        
        /// <summary>
        /// 条件查询
        /// </summary>
        public static IQueryable<{entityName}> WhereIf(this IQueryable<{entityName}> query, bool condition, Expression<Func<{entityName}, bool>> predicate)
        {{
            return condition ? query.Where(predicate) : query;
        }}

        /// <summary>
        /// 按创建时间排序（如果支持审计）
        /// </summary>
        {(IsAudited(entityClass) ? GenerateAuditQueryExtensions(entityName) : string.Empty)}

        /// <summary>
        /// 软删除过滤（如果支持）
        /// </summary>
        {(IsFullyAudited(entityClass) ? GenerateSoftDeleteQueryExtensions(entityName) : string.Empty)}

        // 基于属性的查询扩展
        {GeneratePropertyQueryExtensions(entityName, properties)}
    }}
}}";

            context.AddSource($"{entityName}QueryExtensions.g.cs", SourceText.From(sourceCode, Encoding.UTF8));
