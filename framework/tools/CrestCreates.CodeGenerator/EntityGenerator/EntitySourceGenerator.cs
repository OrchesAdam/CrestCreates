{{
    /// <summary>
    /// {entityName} 实体的仓储接口
    /// </summary>
    public partial interface I{entityName}Repository : IRepository<{entityName}, {idType}>
    {{
        // 基于实体属性的查询方法
        {GenerateRepositoryQueryMethods(entityClass)}

        // 分页查询方法
        Task<(List<{entityName}> Items, int TotalCount)> GetPagedListAsync(
            int pageNumber, 
            int pageSize, 
            Expression<Func<{entityName}, bool>>? predicate = null,
            Expression<Func<{entityName}, object>>? orderBy = null,
            bool ascending = true,
            CancellationToken cancellationToken = default);

