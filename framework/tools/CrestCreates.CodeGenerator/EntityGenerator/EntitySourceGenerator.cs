        }

        private string GenerateAuditQueryExtensions(string entityName)
        {
            return $@"
        public static IQueryable<{entityName}> OrderByCreationTime(this IQueryable<{entityName}> query, bool descending = false)
        {{
            return descending ? query.OrderByDescending(x => x.CreationTime) : query.OrderBy(x => x.CreationTime);
        }}

        public static IQueryable<{entityName}> CreatedAfter(this IQueryable<{entityName}> query, DateTime dateTime)
        {{
            return query.Where(x => x.CreationTime > dateTime);
        }}

        public static IQueryable<{entityName}> CreatedBefore(this IQueryable<{entityName}> query, DateTime dateTime)
        {{
            return query.Where(x => x.CreationTime < dateTime);
        }}";
        }

        private string GenerateSoftDeleteQueryExtensions(string entityName)
        {
            return $@"
        public static IQueryable<{entityName}> NotDeleted(this IQueryable<{entityName}> query)
        {{
            return query.Where(x => !x.IsDeleted);
        }}

        public static IQueryable<{entityName}> OnlyDeleted(this IQueryable<{entityName}> query)
        {{
            return query.Where(x => x.IsDeleted);
        }}";
        }

        private List<IPropertySymbol> GetEntityProperties(INamedTypeSymbol entityClass)
        {
            return entityClass.GetMembers()
                .OfType<IPropertySymbol>()
                .Where(p => p.DeclaredAccessibility == Accessibility.Public &&
                           !p.IsStatic &&
                           p.CanBeReferencedByName &&
                           p.Name != "Id") // 排除主键
                .ToList();
        }

        private string GeneratePropertyQueryExtensions(string entityName, List<IPropertySymbol> properties)
        {
            var extensions = new StringBuilder();

            foreach (var prop in properties)
            {
                if (prop.Type.SpecialType == SpecialType.System_String)
                {
                    extensions.AppendLine($@"
        public static IQueryable<{entityName}> By{prop.Name}(this IQueryable<{entityName}> query, string {prop.Name.ToLowerInvariant()})
        {{
            return query.Where(x => x.{prop.Name} == {prop.Name.ToLowerInvariant()});
        }}

        public static IQueryable<{entityName}> By{prop.Name}Contains(this IQueryable<{entityName}> query, string {prop.Name.ToLowerInvariant()})
        {{
            return query.Where(x => x.{prop.Name}.Contains({prop.Name.ToLowerInvariant()}));
        }}");
                }
                else if (prop.Type.SpecialType != SpecialType.None)
                {
                    extensions.AppendLine($@"
        public static IQueryable<{entityName}> By{prop.Name}(this IQueryable<{entityName}> query, {prop.Type.ToDisplayString()} {prop.Name.ToLowerInvariant()})
        {{
            return query.Where(x => x.{prop.Name} == {prop.Name.ToLowerInvariant()});
        }}");
                }
            }

            return extensions.ToString();
        }

        private string GeneratePropertyMappings(INamedTypeSymbol entityClass, string ormProvider)
        {
            var properties = GetEntityProperties(entityClass);
            var mappings = new StringBuilder();

            foreach (var prop in properties)
            {
                if (ormProvider == "EfCore")
                {
                    if (prop.Type.SpecialType == SpecialType.System_String)
                    {
                        mappings.AppendLine($@"
            builder.Property(e => e.{prop.Name})
                .HasMaxLength(255)
                .IsRequired({prop.NullableAnnotation != NullableAnnotation.Annotated});");
                    }
                }
            }

            return mappings.ToString();
        }

        private string GenerateIndexMappings(INamedTypeSymbol entityClass, string ormProvider)
        {
            // 为常用查询字段生成索引
            var properties = GetEntityProperties(entityClass);
            var indexes = new StringBuilder();

            if (ormProvider == "EfCore")
            {
                foreach (var prop in properties.Take(3)) // 只为前几个属性生成索引
                {
                    if (prop.Type.SpecialType == SpecialType.System_String)
                    {
                        indexes.AppendLine($@"
            builder.HasIndex(e => e.{prop.Name}).HasDatabaseName(""IX_{entityClass.Name}_{prop.Name}"");");
                    }
                }
            }

            return indexes.ToString();
        }

