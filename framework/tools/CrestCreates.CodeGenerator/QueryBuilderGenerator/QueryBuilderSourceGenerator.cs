namespace CrestCreates.CodeGenerator.QueryBuilderGenerator
{
    [Generator]
    public class QueryBuilderSourceGenerator : IIncrementalGenerator
    {
        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            var queryBuilderClasses = context.SyntaxProvider
                .CreateSyntaxProvider(
                    predicate: static (node, _) => IsQueryBuilderCandidate(node),
                    transform: static (ctx, _) => GetQueryBuilderClass(ctx))
                .Where(static x => x is not null)
                .Collect();

            context.RegisterSourceOutput(queryBuilderClasses, ExecuteGeneration);
        }

        private static bool IsQueryBuilderCandidate(SyntaxNode node)
        {
            return node is ClassDeclarationSyntax classDeclaration && classDeclaration.AttributeLists.Count > 0;
        }

        private static INamedTypeSymbol? GetQueryBuilderClass(GeneratorSyntaxContext context)
        {
            var classDeclaration = (ClassDeclarationSyntax)context.Node;
            var symbol = context.SemanticModel.GetDeclaredSymbol(classDeclaration) as INamedTypeSymbol;

            if (symbol != null && HasGenerateQueryBuilderAttribute(symbol))
            {
                return symbol;
            }

            return null;
        }

        private static bool HasGenerateQueryBuilderAttribute(INamedTypeSymbol symbol)
        {
            return symbol.GetAttributes().Any(attr =>
                attr.AttributeClass?.Name == "GenerateQueryBuilderAttribute" ||
                attr.AttributeClass?.Name == "GenerateQueryBuilder");
        }

        private void ExecuteGeneration(SourceProductionContext context, ImmutableArray<INamedTypeSymbol?> entityClasses)
        {
            if (entityClasses.IsDefaultOrEmpty) return;

            var processedEntities = new HashSet<string>();

            foreach (var entityClass in entityClasses)
            {
                if (entityClass == null) continue;

                var entityFullName = entityClass.ToDisplayString();
                if (processedEntities.Contains(entityFullName)) continue;

                processedEntities.Add(entityFullName);

                try
                {
                    GenerateFilterBuilder(context, entityClass);
                    GenerateSortBuilder(context, entityClass);
                    GenerateQueryRequest(context, entityClass);
                    GenerateQueryExecutor(context, entityClass);
                }
                catch (Exception ex)
                {
                    context.ReportDiagnostic(Diagnostic.Create(
                        new DiagnosticDescriptor("CCQB001", "QueryBuilder generation error",
                            $"Error generating query builder for {entityFullName}: {ex.Message}",
                            "CodeGeneration", DiagnosticSeverity.Warning, true),
                        Location.None));
                }
            }
        }

