            sourceCode.AppendLine("        /// <summary>");
            sourceCode.AppendLine("        /// 应用排序");
            sourceCode.AppendLine("        /// </summary>");
            sourceCode.AppendLine($"        public static IQueryable<{entityName}> ApplySorts(IQueryable<{entityName}> query, {entityName}QueryRequest request)");
