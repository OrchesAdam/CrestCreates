            sourceCode.AppendLine("        /// <summary>");
            sourceCode.AppendLine("        /// 应用单个排序");
            sourceCode.AppendLine("        /// </summary>");
            sourceCode.AppendLine($"        private static IQueryable<{entityName}> ApplySort(IQueryable<{entityName}> query, SortDescriptor sort, ref bool isFirstSort)");
            sourceCode.AppendLine("        {");
            sourceCode.AppendLine($"            var parameter = Expression.Parameter(typeof({entityName}), \"x\");");
            sourceCode.AppendLine("            var property = Expression.Property(parameter, sort.Field);");
            sourceCode.AppendLine("            var lambda = Expression.Lambda(property, parameter);");
            sourceCode.AppendLine();
            sourceCode.AppendLine("            var methodName = isFirstSort");
            sourceCode.AppendLine("                ? (sort.Direction == SortDirection.Ascending ? \"OrderBy\" : \"OrderByDescending\")");
            sourceCode.AppendLine("                : (sort.Direction == SortDirection.Ascending ? \"ThenBy\" : \"ThenByDescending\");");
            sourceCode.AppendLine();
            sourceCode.AppendLine("            var method = typeof(Queryable).GetMethods()");
            sourceCode.AppendLine("                .Where(m => m.Name == methodName && m.GetParameters().Length == 2)");
            sourceCode.AppendLine("                .Single()");
            sourceCode.AppendLine($"                .MakeGenericMethod(typeof({entityName}), property.Type);");
            sourceCode.AppendLine();
            sourceCode.AppendLine($"            query = (IQueryable<{entityName}>)method.Invoke(null, new object[] {{ query, lambda }});");
            sourceCode.AppendLine("            isFirstSort = false;");
            sourceCode.AppendLine();
            sourceCode.AppendLine("            return query;");
            sourceCode.AppendLine("        }");
