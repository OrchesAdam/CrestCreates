        private void GenerateQueryExecutor(SourceProductionContext context, INamedTypeSymbol entityClass)
        {
            var entityName = entityClass.Name;
            var namespaceName = entityClass.ContainingNamespace.ToDisplayString();
            var properties = GetQueryableProperties(entityClass);

            var sourceCode = new StringBuilder();
            sourceCode.AppendLine("#nullable enable");
            sourceCode.AppendLine("// <auto-generated />");
            sourceCode.AppendLine("using System;");
            sourceCode.AppendLine("using System.Collections.Generic;");
            sourceCode.AppendLine("using System.Linq;");
            sourceCode.AppendLine("using System.Linq.Expressions;");
            sourceCode.AppendLine("using System.Reflection;");
            sourceCode.AppendLine("using System.Threading;");
            sourceCode.AppendLine("using System.Threading.Tasks;");
            sourceCode.AppendLine("using CrestCreates.Domain.Shared.DTOs;");
            sourceCode.AppendLine("using CrestCreates.Domain.Shared.Enums;");
            sourceCode.AppendLine($"using {namespaceName};");
            sourceCode.AppendLine($"using {namespaceName}.QueryBuilders;");
            sourceCode.AppendLine();
            sourceCode.AppendLine($"namespace {namespaceName}.QueryBuilders");
            sourceCode.AppendLine("{");
            sourceCode.AppendLine($"    /// <summary>");
            sourceCode.AppendLine($"    /// {entityName} 的查询执行器");
            sourceCode.AppendLine($"    /// </summary>");
            sourceCode.AppendLine($"    public class {entityName}QueryExecutor");
            sourceCode.AppendLine("    {");
            sourceCode.AppendLine("        /// <summary>");
            sourceCode.AppendLine("        /// 执行查询");
            sourceCode.AppendLine("        /// </summary>");
            sourceCode.AppendLine($"        public static IQueryable<{entityName}> ApplyFilters(IQueryable<{entityName}> query, {entityName}QueryRequest request)");
            sourceCode.AppendLine("        {");
            sourceCode.AppendLine("            if (request?.Filters == null || !request.Filters.Any())");
            sourceCode.AppendLine("                return query;");
            sourceCode.AppendLine();
            sourceCode.AppendLine("            foreach (var filter in request.Filters)");
            sourceCode.AppendLine("            {");
            sourceCode.AppendLine("                query = ApplyFilter(query, filter);");
            sourceCode.AppendLine("            }");
            sourceCode.AppendLine();
            sourceCode.AppendLine("            return query;");
            sourceCode.AppendLine("        }");
            sourceCode.AppendLine();

            sourceCode.AppendLine("        /// <summary>");
            sourceCode.AppendLine("        /// 应用单个过滤器");
            sourceCode.AppendLine("        /// </summary>");
            sourceCode.AppendLine($"        private static IQueryable<{entityName}> ApplyFilter(IQueryable<{entityName}> query, FilterDescriptor filter)");
            sourceCode.AppendLine("        {");
            sourceCode.AppendLine($"            var parameter = Expression.Parameter(typeof({entityName}), \"x\");");
            sourceCode.AppendLine("            var property = Expression.Property(parameter, filter.Field);");
            sourceCode.AppendLine("            var constant = Expression.Constant(filter.Value);");
            sourceCode.AppendLine();
            sourceCode.AppendLine("            Expression? predicate = null;");
            sourceCode.AppendLine();
            sourceCode.AppendLine("            switch (filter.Operator)");
            sourceCode.AppendLine("            {");
            sourceCode.AppendLine("                case FilterOperator.Equals:");
            sourceCode.AppendLine("                    predicate = Expression.Equal(property, constant);");
            sourceCode.AppendLine("                    break;");
            sourceCode.AppendLine("                case FilterOperator.NotEquals:");
            sourceCode.AppendLine("                    predicate = Expression.NotEqual(property, constant);");
            sourceCode.AppendLine("                    break;");
            sourceCode.AppendLine("                case FilterOperator.GreaterThan:");
            sourceCode.AppendLine("                    predicate = Expression.GreaterThan(property, constant);");
            sourceCode.AppendLine("                    break;");
            sourceCode.AppendLine("                case FilterOperator.GreaterThanOrEqual:");
            sourceCode.AppendLine("                    predicate = Expression.GreaterThanOrEqual(property, constant);");
            sourceCode.AppendLine("                    break;");
            sourceCode.AppendLine("                case FilterOperator.LessThan:");
            sourceCode.AppendLine("                    predicate = Expression.LessThan(property, constant);");
            sourceCode.AppendLine("                    break;");
            sourceCode.AppendLine("                case FilterOperator.LessThanOrEqual:");
            sourceCode.AppendLine("                    predicate = Expression.LessThanOrEqual(property, constant);");
            sourceCode.AppendLine("                    break;");
            sourceCode.AppendLine("                case FilterOperator.Contains:");
            sourceCode.AppendLine("                    var containsMethod = typeof(string).GetMethod(\"Contains\", new[] { typeof(string) });");
            sourceCode.AppendLine("                    if (containsMethod != null)");
            sourceCode.AppendLine("                        predicate = Expression.Call(property, containsMethod, constant);");
            sourceCode.AppendLine("                    break;");
            sourceCode.AppendLine("                case FilterOperator.StartsWith:");
            sourceCode.AppendLine("                    var startsWithMethod = typeof(string).GetMethod(\"StartsWith\", new[] { typeof(string) });");
            sourceCode.AppendLine("                    if (startsWithMethod != null)");
            sourceCode.AppendLine("                        predicate = Expression.Call(property, startsWithMethod, constant);");
            sourceCode.AppendLine("                    break;");
            sourceCode.AppendLine("                case FilterOperator.EndsWith:");
            sourceCode.AppendLine("                    var endsWithMethod = typeof(string).GetMethod(\"EndsWith\", new[] { typeof(string) });");
            sourceCode.AppendLine("                    if (endsWithMethod != null)");
            sourceCode.AppendLine("                        predicate = Expression.Call(property, endsWithMethod, constant);");
            sourceCode.AppendLine("                    break;");
            sourceCode.AppendLine("            }");
            sourceCode.AppendLine();
            sourceCode.AppendLine("            if (predicate != null)");
            sourceCode.AppendLine("            {");
            sourceCode.AppendLine($"                var lambda = Expression.Lambda<Func<{entityName}, bool>>(predicate, parameter);");
            sourceCode.AppendLine("                query = query.Where(lambda);");
