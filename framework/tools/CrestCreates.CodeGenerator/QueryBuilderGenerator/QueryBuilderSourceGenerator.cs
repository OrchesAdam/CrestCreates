        private List<IPropertySymbol> GetQueryableProperties(INamedTypeSymbol entityClass)
        {
            return entityClass.GetMembers()
                .OfType<IPropertySymbol>()
                .Where(p => p.DeclaredAccessibility == Accessibility.Public &&
                           !p.IsStatic &&
                           p.CanBeReferencedByName &&
                           p.Name != "Id" &&
                           !IsNavigationProperty(p))
                .ToList();
        }

        private bool IsNavigationProperty(IPropertySymbol property)
        {
            var type = property.Type;

            // 检查是否是集合类型（导航集合）
            if (type is INamedTypeSymbol namedType)
            {
                if (namedType.IsGenericType)
                {
                    var genericTypeName = namedType.ConstructedFrom.ToDisplayString();
                    if (genericTypeName.StartsWith("System.Collections") ||
                        genericTypeName.StartsWith("System.Collections.Generic"))
                    {
                        return true;
                    }
                }

                // 检查是否是复杂类型（可能是导航属性）
                if (namedType.TypeKind == TypeKind.Class &&
                    !namedType.IsValueType &&
                    namedType.SpecialType == SpecialType.None &&
                    !IsPrimitiveType(namedType))
                {
                    return true;
                }
            }

            return false;
        }

        private bool IsPrimitiveType(INamedTypeSymbol type)
        {
            var primitiveTypes = new[]
            {
                SpecialType.System_Boolean,
                SpecialType.System_Char,
                SpecialType.System_SByte,
                SpecialType.System_Byte,
                SpecialType.System_Int16,
                SpecialType.System_UInt16,
                SpecialType.System_Int32,
                SpecialType.System_UInt32,
                SpecialType.System_Int64,
                SpecialType.System_UInt64,
                SpecialType.System_Decimal,
                SpecialType.System_Single,
                SpecialType.System_Double,
                SpecialType.System_String,
                SpecialType.System_DateTime
            };

            return primitiveTypes.Contains(type.SpecialType) || type.Name == "Guid";
        }

        private bool IsStringType(ITypeSymbol type)
        {
            return type.SpecialType == SpecialType.System_String;
        }

        private bool IsNumericOrDateType(ITypeSymbol type)
        {
            var numericTypes = new[]
            {
                SpecialType.System_Int16,
                SpecialType.System_Int32,
                SpecialType.System_Int64,
                SpecialType.System_UInt16,
                SpecialType.System_UInt32,
                SpecialType.System_UInt64,
                SpecialType.System_Decimal,
                SpecialType.System_Single,
                SpecialType.System_Double,
                SpecialType.System_Byte,
                SpecialType.System_SByte,
                SpecialType.System_DateTime
            };

            return numericTypes.Contains(type.SpecialType);
        }
