    protected override void Execute(Compilation compilation, ImmutableArray<ClassAnalysisInfo> classes, SourceProductionContext context)
    {
        if (!classes.Any())
        {
            return;
        }

        var moduleInfos = classes.Select(c => new ModuleInfo(
            c.FullName,
            c.ClassName,
            GetModuleDependencies(c, compilation)
        )).ToList();

        var source = GenerateModuleRegistrarCode(moduleInfos);
        context.AddSource($"{GeneratedClassName}.g.cs", source);
    }
