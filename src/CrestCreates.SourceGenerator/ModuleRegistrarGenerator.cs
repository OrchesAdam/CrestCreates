// 模块信息记录类，用于生成模块注册代码
public class ModuleInfo
{
    public string TypeName { get; }
    public string ClassName { get; }
    public List<string> Dependencies { get; }

    public ModuleInfo(string typeName, string className, List<string> dependencies)
    {
        TypeName = typeName;
        ClassName = className;
        Dependencies = dependencies;
    }
}

[Generator]
