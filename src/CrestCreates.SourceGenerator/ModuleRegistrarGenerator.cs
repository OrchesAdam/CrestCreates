    protected override ClassAnalysisInfo? AnalyzeClass(GeneratorSyntaxContext context)
    {
        // 确保我们处理的是类声明
        if (context.Node is not ClassDeclarationSyntax classDeclaration)
        {
            return null;
        }

        // 获取语义模型，以便我们能访问符号信息
        var semanticModel = context.SemanticModel;
        var classSymbol = semanticModel.GetDeclaredSymbol(classDeclaration) as INamedTypeSymbol;
        if (classSymbol == null)
        {
            return null;
        }

        // 查找类上的ModuleAttribute特性
        var moduleAttribute = classSymbol.GetAttributes()
            .FirstOrDefault(attr => attr.AttributeClass?.Name == ClassAttributeName ||
                                   attr.AttributeClass?.Name == ClassAttributeName.Replace("Attribute", ""));

        // 如果没有找到特性，直接返回
        if (moduleAttribute == null)
        {
            return null;
        }

        // 获取模块的依赖项（如果有的话）
        var dependencyTypes = new List<string>();
        if (moduleAttribute.ConstructorArguments.Any())
        {
            var dependencies = moduleAttribute.ConstructorArguments.First();
            if (dependencies.Kind == TypedConstantKind.Array && dependencies.Values.Any())
            {
                foreach (var dep in dependencies.Values)
                {
                    if (dep.Value is ITypeSymbol typeSymbol)
                    {
                        dependencyTypes.Add(typeSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat));
                    }
                }
            }
        }

        // 检查是否实现了ICrestCreatesModule接口
        var implementsICrestCreatesModule = classSymbol.AllInterfaces.Any(i => i.Name == "ICrestCreatesModule");

        // 获取所有实现的接口
        var implementedInterfaces = classSymbol.AllInterfaces
            .Select(i => i.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat))
            .ToList();

        // 创建服务特性信息
        var serviceAttributes = new List<ServiceAttributeInfo>
        {
            new ServiceAttributeInfo(
                classSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat),
                "Singleton",
                true,
                false
            )
        };

        // 创建分析结果
        return new ClassAnalysisInfo(
            classSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat),
            classSymbol.Name,
            implementsICrestCreatesModule, // 如果实现了ICrestCreatesModule接口，则标记为服务
            ["Module"],
            new ServiceDescriptorInfo(
                classSymbol.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat),
                classSymbol.Name,
                classSymbol.ContainingNamespace.ToDisplayString(),
                serviceAttributes,
                null,
                null,
                implementedInterfaces
            )
        );
    }

    protected override bool FilterClass(ClassAnalysisInfo? info)
    {
        if (info == null)
        {
            return false;
        }

        // 过滤出标记了Module特性且实现了ICrestCreatesModule接口的类
        return info.AttributeNames.Contains("Module");
    }

