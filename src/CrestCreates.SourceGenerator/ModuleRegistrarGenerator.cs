    private static List<ModuleInfo> SortModulesByDependencies(List<ModuleInfo> modules)
    {
        // 创建模块名称到模块信息的映射
        var moduleMap = modules.ToDictionary(m => m.TypeName);
        
        // 创建已访问的模块跟踪和记录已完成排序的模块
        var visited = new Dictionary<string, bool>();
        var temporaryMarked = new HashSet<string>(); // 用于检测循环依赖
        var result = new List<ModuleInfo>();
        
        // 首先确保所有依赖项都是有效的（在moduleMap中存在）
        foreach (var module in modules)
        {
            var validDependencies = new List<string>();
            foreach (var dep in module.Dependencies)
            {
                if (moduleMap.ContainsKey(dep))
                {
                    validDependencies.Add(dep);
                }
                else
                {
                    // 记录无效依赖项，这些依赖可能是外部模块或者拼写错误
                    System.Diagnostics.Debug.WriteLine($"Warning: Module {module.TypeName} depends on {dep}, but it's not found in registered modules");
                }
            }
            
            // 更新模块的依赖项列表，只保留有效的依赖
            if (module.Dependencies.Count != validDependencies.Count)
            {
                module.Dependencies.Clear();
                module.Dependencies.AddRange(validDependencies);
            }
        }

        // 对每个模块进行深度优先搜索
        foreach (var module in modules)
        {
            if (!visited.ContainsKey(module.TypeName) && !temporaryMarked.Contains(module.TypeName))
            {
                VisitModuleEnhanced(module, moduleMap, visited, temporaryMarked, result);
            }
        }

        return result;
    }

    /// <summary>
    /// 增强版的拓扑排序递归辅助方法，支持循环依赖检测
    /// </summary>
    private static void VisitModuleEnhanced(ModuleInfo module,
        Dictionary<string, ModuleInfo> moduleMap,
        Dictionary<string, bool> visited,
        HashSet<string> temporaryMarked,
        List<ModuleInfo> result)
    {
        // 如果已经在结果列表中，直接返回
        if (result.Contains(module))
        {
            return;
        }
        
        // 检查是否有循环依赖
        if (!temporaryMarked.Add(module.TypeName))
        {
            // 发现循环依赖，但为了不中断流程，我们记录警告并继续
            throw new InvalidOperationException($"Circular dependency detected for module {module.TypeName}. Please review the module dependencies.");
        }
        
        // 临时标记为正在访问

        // 先访问所有依赖项
        foreach (var dependency in module.Dependencies)
        {
            // 如果依赖项存在于模块映射中且尚未被访问
            if (moduleMap.TryGetValue(dependency, out var dependencyModule) && 
                !result.Contains(dependencyModule))
            {
                VisitModuleEnhanced(dependencyModule, moduleMap, visited, temporaryMarked, result);
            }
        }
        
        // 移除临时标记
        temporaryMarked.Remove(module.TypeName);
        
        // 标记为已完成访问
        visited[module.TypeName] = true;
        
        // 将模块添加到结果中（如果尚未添加）
        if (!result.Contains(module))
        {
            result.Add(module);
        }
