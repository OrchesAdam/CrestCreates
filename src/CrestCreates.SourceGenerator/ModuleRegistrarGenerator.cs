    protected override string ClassAttributeName => nameof(ModuleAttribute);
    protected override string GeneratedClassName => "GeneratedModuleRegistrar";
    protected override string GetAttributeSource()
    {
        // ModuleAttribute 已经在 CrestCreates.Modularity 命名空间中定义，不需要生成
        return string.Empty;
