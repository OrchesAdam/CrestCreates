using System.Collections.Immutable;
using System.Linq;
using System.Text;
using CrestCreates.WebApp.SourceGenerator.CodeAnalyzer;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;

namespace CrestCreates.WebApp.SourceGenerator
{
    /// <summary>
    /// 控制器端点生成器
    /// </summary>
    [Generator]
    public class ControllerEndpointsGenerator : CommonIncrementalGenerator
    {
        /// <summary>
        /// 获取生成器名称
        /// </summary>
        protected override string GeneratorName => "ControllerEndpointsGenerator";
        
        /// <summary>
        /// 获取生成的属性名称
        /// </summary>
        protected override string ClassAttributeName => "ControllerEndpointsAttribute";
        
        /// <summary>
        /// 获取生成的类名
        /// </summary>
        protected override string GeneratedClassName => "GeneratedControllerEndpoints";

        /// <summary>
        /// 获取生成的属性源代码
        /// </summary>
        protected override string GetAttributeSource()
        {
            return $$"""
                     // <auto-generated/>
                     #nullable enable
                     using System;

                     namespace CrestCreates.SourceGenerator
                     {
                         [AttributeUsage(AttributeTargets.Assembly)]
                         internal sealed class {{ClassAttributeName}} : Attribute
                         {
                             public Type RegistrarType { get; }
                             
                             public {{ClassAttributeName}}(Type registrarType)
                             {
                                 RegistrarType = registrarType;
                             }
                         }
                     }
                     
                     """;
        }

        /// <summary>
        /// 过滤出有Controller属性的类
        /// </summary>
        protected override bool FilterClass(ClassAnalysisInfo? info)
        {
            if (info == null) return false;
            return info.AttributeNames.Any(attr => attr.Contains("Controller") || attr.Contains("ApiController"));
        }

        /// <summary>
        /// 分析类是否满足生成条件
        /// </summary>
        protected override ClassAnalysisInfo? AnalyzeClass(GeneratorSyntaxContext context)
        {
            if (context.Node is not ClassDeclarationSyntax classDeclaration)
                return null;

            var classSymbol = context.SemanticModel.GetDeclaredSymbol(classDeclaration);
            if (classSymbol == null)
                return null;

            var attributes = classSymbol.GetAttributes();
            var attrNames = attributes.Select(a => a.AttributeClass?.Name ?? "Unknown").ToList();
            var isController = classSymbol.Name.EndsWith("Controller") ||
                              attrNames.Any(a => a.Contains("Controller") || a.Contains("ApiController"));

            return new ClassAnalysisInfo(
                classSymbol.ToDisplayString(),
                classSymbol.Name,
                isController,
                attrNames,
                null // 控制器不需要服务信息
            );
        }

        /// <summary>
        /// 生成调试信息
        /// </summary>
        protected override string GenerateDebugInfo()
        {
            return GenerateDefaultDebugInfo("CrestCreates.Generated.Controllers");
        }

        /// <summary>
        /// 生成调试报告
        /// </summary>
        protected override void GenerateDebugReport(Compilation compilation, ImmutableArray<ClassAnalysisInfo?> classes,
            SourceProductionContext context)
        {
            var validClasses = classes.Where(c => c != null).Cast<ClassAnalysisInfo>().ToList();
            var controllerClasses = validClasses.Where(c => c.HasServiceAttributes || c.ClassName.EndsWith("Controller")).ToList();

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("// Debug report for controller endpoints generator");
            sb.AppendLine();
            sb.AppendLine("/*");
            sb.AppendLine($"Total classes analyzed: {validClasses.Count}");
            sb.AppendLine($"Controller classes found: {controllerClasses.Count}");
            sb.AppendLine();
            sb.AppendLine("Controller classes:");

            foreach (var controller in controllerClasses)
            {
                sb.AppendLine($"  - {controller.ClassName}");
                sb.AppendLine($"    Attributes: {string.Join(", ", controller.AttributeNames)}");
            }

            sb.AppendLine("*/");

            context.AddSource($"{GeneratorName}DebugReport.g.cs", sb.ToString());
        }

        /// <summary>
        /// 执行代码生成
        /// </summary>
        protected override void Execute(Compilation compilation, ImmutableArray<ClassAnalysisInfo> controllers,
            SourceProductionContext context)
        {
            if (!controllers.Any())
            {
                return;
            }

            var source = GenerateControllerEndpointsCode(controllers);
            context.AddSource($"{GeneratedClassName}.g.cs", source);
        }

        private string GenerateControllerEndpointsCode(ImmutableArray<ClassAnalysisInfo> controllers)
        {
            var sb = new StringBuilder();

            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("#nullable enable");
            sb.AppendLine("using System;");
            sb.AppendLine("using System.Collections.Generic;");
            sb.AppendLine();

            // 生成命名空间
            sb.AppendLine("namespace CrestCreates.Generated.Controllers");
            sb.AppendLine("{");
            
            sb.AppendLine("    /// <summary>");
            sb.AppendLine("    /// 自动生成的控制器端点信息");
            sb.AppendLine("    /// </summary>");
            sb.AppendLine($"    public static class {GeneratedClassName}");
            sb.AppendLine("    {");
            sb.AppendLine("        /// <summary>");
            sb.AppendLine("        /// 获取所有控制器信息");
            sb.AppendLine("        /// </summary>");
            sb.AppendLine("        public static IReadOnlyList<ControllerInfo> Controllers { get; } = new List<ControllerInfo>");
            sb.AppendLine("        {");

            foreach (var controller in controllers)
            {
                sb.AppendLine($"            new ControllerInfo(\"{controller.FullName}\", \"{controller.ClassName}\"),");
            }

            sb.AppendLine("        };");
            sb.AppendLine("    }");

            sb.AppendLine();
            sb.AppendLine("    /// <summary>");
            sb.AppendLine("    /// 控制器信息");
            sb.AppendLine("    /// </summary>");
            sb.AppendLine("    public class ControllerInfo");
            sb.AppendLine("    {");
            sb.AppendLine("        /// <summary>");
            sb.AppendLine("        /// 控制器全名");
            sb.AppendLine("        /// </summary>");
            sb.AppendLine("        public string FullName { get; }");
            sb.AppendLine();
            sb.AppendLine("        /// <summary>");
            sb.AppendLine("        /// 控制器名称");
            sb.AppendLine("        /// </summary>");
            sb.AppendLine("        public string Name { get; }");
            sb.AppendLine();
            sb.AppendLine("        /// <summary>");
            sb.AppendLine("        /// 初始化控制器信息");
            sb.AppendLine("        /// </summary>");
            sb.AppendLine("        public ControllerInfo(string fullName, string name)");
            sb.AppendLine("        {");
            sb.AppendLine("            FullName = fullName;");
            sb.AppendLine("            Name = name;");
            sb.AppendLine("        }");
            sb.AppendLine("    }");
            sb.AppendLine("}");

            return sb.ToString();
        }
    }
}
